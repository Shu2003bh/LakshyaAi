"use server";

import { db } from "@/lib/prisma";
import { auth } from "@clerk/nextjs/server";
import Groq from "groq-sdk";

const groq = new Groq({
  apiKey: process.env.GROQ_API_KEY,
});

/* ----------------------------------
   GENERATE COVER LETTER
-----------------------------------*/
export async function generateCoverLetter(data) {
  const { userId } = await auth();
  if (!userId) throw new Error("Unauthorized");

  const user = await db.user.findUnique({
    where: { clerkUserId: userId },
  });

  if (!user) throw new Error("User not found");

  const prompt = `
Write a professional cover letter for a ${data.jobTitle} position at ${data.companyName}.

Candidate details:
- Industry: ${user.industry}
- Years of Experience: ${user.experience}
- Skills: ${user.skills?.join(", ")}
- Professional Background: ${user.bio}

Job Description:
${data.jobDescription}

Requirements:
- Professional and enthusiastic tone
- Highlight relevant skills and experience
- Align candidate background with job needs
- Max 400 words
- Proper business letter formatting in Markdown
- Include 1–2 concrete achievements
`;

  try {
    const res = await groq.chat.completions.create({
      model: "llama-3.1-70b-versatile", // better writing quality
      temperature: 0.6,
      max_tokens: 900,
      messages: [
        {
          role: "system",
          content:
            "You are an expert career coach and professional resume writer.",
        },
        { role: "user", content: prompt },
      ],
    });

    const content = res.choices[0].message.content.trim();

    const coverLetter = await db.coverLetter.create({
      data: {
        content,
        jobDescription: data.jobDescription,
        companyName: data.companyName,
        jobTitle: data.jobTitle,
        status: "completed",
        userId: user.id,
      },
    });

    return coverLetter;
  } catch (error) {
    console.error("Error generating cover letter:", error);
    throw new Error("Failed to generate cover letter");
  }
}

/* ----------------------------------
   GET ALL COVER LETTERS
-----------------------------------*/
export async function getCoverLetters() {
  const { userId } = await auth();
  if (!userId) throw new Error("Unauthorized");

  const user = await db.user.findUnique({
    where: { clerkUserId: userId },
  });

  if (!user) throw new Error("User not found");

  return await db.coverLetter.findMany({
    where: { userId: user.id },
    orderBy: { createdAt: "desc" },
  });
}

